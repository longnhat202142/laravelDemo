              <h2>Khóa bồi dưỡng giảng viên Khu vực miền Trung - Tây Nguyên năm 2024 “Thống kê hiện đại với phần mềm thống kê R”</h2>
              <p class="box-straight-line">03-05-2004</p>
              <p >Từ ngày 3-5/5, tại Hội trường A1, Trường Đại học Khoa học (ĐHKH), Đại học Huế đã phối hợp với Viện Nghiên cứu cao cấp về Toán tổ chức Khóa bồi dưỡng giảng viên Khu vực miền Trung - Tây Nguyên năm 2024 “Thống kê hiện đại với phần mềm thống kê R”.

Tham dự có: PGS.TS. Lê Minh Hà, Giám đốc điều hành Viện Nghiên cứu cao cấp về Toán; TS. Đỗ Thị Xuân Dung, Phó Giám đốc Đại học Huế;  TS. Bùi Quang Vũ, Chủ tịch Hội đồng Trường Đại học Khoa học, Đại học Huế; các giảng viên của Khóa bồi dưỡng và hơn 120 học viên đến từ các trường thuộc khu vực miền Trung – Tây Nguyên.
Khóa bồi dưỡng nằm trong trong kế hoạch năm 2024 của Chương trình trọng điểm quốc gia phát triển Toán học giai đoạn 2021 – 2030, Khóa bồi dưỡng giảng viên khu vực miền Trung - Tây Nguyên năm 2024 với chủ đề “Thống kê hiện đại với phần mềm thống kê R”.
Khóa được tổ chức theo hình thức: trực tuyến (25-26/4/2024) và trực tiếp (03-05/5/2024) tại Trường ĐKHH, Đại học Huế.  
Khóa bồi dưỡng nhằm trang bị và củng cố các kiến thức cơ bản về thống kê và vận dụng phần mềm R để phân tích, xử lý dữ liệu. Đặc biệt chú ý đến kỹ năng sử dụng phần mềm R, việc diễn giải các kết quả thu được khi chạy phần mềm và nhận biết các sai lầm thường gặp. Học viên được hướng dẫn thực hành xử lý số liệu thực tế bằng phần mềm thống kê R qua các dự án cụ thể.
